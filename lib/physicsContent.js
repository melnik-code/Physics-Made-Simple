export function getSection(sectionSlug) {
  return sections.find((section) => section.slug === sectionSlug);
}

export function getGroup(sectionSlug, groupSlug) {
  const section = getSection(sectionSlug);
  return section?.groups.find((group) => group.slug === groupSlug);
}

export function getTopic(sectionSlug, groupSlug, topicSlug) {
  const section = getSection(sectionSlug);
  const group = section?.groups.find((item) => item.slug === groupSlug);
  const topic = group?.topics.find((item) => item.slug === topicSlug);

  if (!section || !group || !topic) return null;

  return {
    ...topic,
    sectionSlug: section.slug,
    sectionTitle: section.title,
    groupSlug: group.slug,
    groupTitle: group.title,
    accent: section.accent,
    href: `/topics/${section.slug}/${group.slug}/${topic.slug}`
  };
}

export function getTopicNeighbors(sectionSlug, groupSlug, topicSlug) {
  const group = getGroup(sectionSlug, groupSlug);
  if (!group) return { previous: null, next: null };

  const index = group.topics.findIndex((topic) => topic.slug === topicSlug);
  const makeLink = (topic) =>
    topic
      ? {
          title: topic.title,
          href: `/topics/${sectionSlug}/${groupSlug}/${topic.slug}`
        }
      : null;

  return {
    previous: makeLink(group.topics[index - 1]),
    next: makeLink(group.topics[index + 1])
  };
}

export function getTopicFormulas(sectionSlug, groupSlug, topicSlug) {
  const topic = getTopic(sectionSlug, groupSlug, topicSlug);
  if (!topic) return [];

  return topic.formulas.map((formula) => ({
    ...formula,
    sectionSlug,
    topicTitle: topic.title,
    topicHref: topic.href,
    accent: topic.accent
  }));
}

